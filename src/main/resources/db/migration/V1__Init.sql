CREATE TABLE price (
  id BIGINT AUTO_INCREMENT NOT NULL,
   brand_id BIGINT NOT NULL,
   start_date VARCHAR(19),
   end_date VARCHAR(19),
   price_list INT NOT NULL,
   product_id BIGINT NOT NULL,
   priority INT NOT NULL,
   price DECIMAL,
   curr VARCHAR(3),
   CONSTRAINT pk_price PRIMARY KEY (id)
);

